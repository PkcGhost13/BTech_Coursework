create table CRSSECTION
(
    CSID      NUMBER generated as identity,
    COURSEID  NUMBER
        unique,
    SECTION   VARCHAR2(5),
    TERMID    NUMBER,
    FACULTYID NUMBER,
    DAY       VARCHAR2(10),
    STARTTIME VARCHAR2(10),
    ENDTIME   NUMBER,
    ROOMID    NUMBER,
    MAXCOUNT  NUMBER
        check (MaxCount <= 10)
)
